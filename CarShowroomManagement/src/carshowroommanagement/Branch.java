package carshowroommanagement;

public class Branch {
 
	private String branchName;
	private String cityName;
	private int availableStocks;
	private int soldStocks;
	
	public String getBranchName() {
		return branchName;
	}
	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public int getAvailableStocks() {
		return availableStocks;
	}
	public void setAvailableStocks(int availableStocks) {
		this.availableStocks = availableStocks;
	}
	public int getSoldStocks() {
		return soldStocks;
	}
	public void setSoldStocks(int soldStocks) {
		this.soldStocks = soldStocks;
	}
	
	
	
	
}
